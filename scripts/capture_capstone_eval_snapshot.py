#!/usr/bin/env python3
"""Regenerate docs/capstone-demo-artifacts.md using ASGI (no uvicorn port required)."""

from __future__ import annotations

import json
import os
import sys
import tempfile
from datetime import datetime, timezone
from pathlib import Path

from starlette.testclient import TestClient

REPO = Path(__file__).resolve().parents[1]


class _TestClientShim:
    """Minimal surface used by ``evaluate(..., client=...)``."""

    def __init__(self, tc: TestClient) -> None:
        self._tc = tc

    def post(self, url: str, json: object | None = None):  # noqa: ANN201
        base = "http://testserver"
        path = url[len(base) :] if url.startswith(base) else url
        if not path.startswith("/"):
            path = "/" + path
        return self._tc.post(path, json=json)


def main() -> int:
    os.chdir(REPO)
    fd, dbpath = tempfile.mkstemp(suffix=".db")
    os.close(fd)
    os.environ["FLEET_DB_PATH"] = dbpath

    sys.path.insert(0, str(REPO / "services" / "orchestrator" / "src"))
    from fleet_health_orchestrator.main import app  # noqa: E402

    sys.path.insert(0, str(REPO / "services" / "orchestrator" / "scripts"))
    from evaluate_pipeline import evaluate  # noqa: E402

    runbooks = REPO / "services/orchestrator/data/runbooks_detailed.jsonl"
    events = REPO / "services/orchestrator/data/sample_events.jsonl"
    out = REPO / "docs/capstone-demo-artifacts.md"
    base = "http://testserver"
    with TestClient(app) as tc:
        for line in runbooks.read_text(encoding="utf-8").splitlines():
            if not line.strip():
                continue
            r = tc.post("/v1/rag/documents", json=json.loads(line))
            r.raise_for_status()
        metrics = evaluate(events, base, client=_TestClientShim(tc))

    when = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    body = (
        "# Capstone demo evaluation snapshot\n\n"
        "This file is generated by "
        "`PYTHONPATH=services/orchestrator/src .venv/bin/python "
        "scripts/capture_capstone_eval_snapshot.py` "
        "from the current [runbooks_detailed.jsonl](../services/orchestrator/data/runbooks_detailed.jsonl) "
        "and [sample_events.jsonl](../services/orchestrator/data/sample_events.jsonl). "
        "It records a reproducible **`evaluate_pipeline`** outcome without starting Uvicorn.\n\n"
        f"**Generated (UTC):** {when}\n\n"
        "## Metrics (`evaluate_pipeline.py`)\n\n"
        "```json\n"
        f"{json.dumps(metrics, indent=2)}\n"
        "```\n\n"
        "## Live demo commands\n\n"
        "For the full operator path (orchestrator on port 8000), see "
        "[demo-runbook.md](demo-runbook.md).\n"
    )
    out.write_text(body, encoding="utf-8")
    print(f"wrote {out}")
    Path(dbpath).unlink(missing_ok=True)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
